package corse;

public class CorseDTO {
	private String corse;
	private long lon;
	private long lat;
	private long elev;
	public String getCorse() {
		return corse;
	}
	public void setCorse(String corse) {
		this.corse = corse;
	}
	public long getLon() {
		return lon;
	}
	public void setLon(long lon) {
		this.lon = lon;
	}
	public long getLat() {
		return lat;
	}
	public void setLat(long lat) {
		this.lat = lat;
	}
	public long getElev() {
		return elev;
	}
	public void setElev(long elev) {
		this.elev = elev;
	}
	
}
